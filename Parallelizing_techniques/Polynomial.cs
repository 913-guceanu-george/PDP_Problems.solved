        static int[] MultiplyPolynomialsRegularSequential(int[] poly1, int[] poly2)
        {
            int degree = poly1.Length + poly2.Length - 2;
            int[] result = new int[degree + 1];

            for (int i = 0; i < poly1.Length; i++)
            {
                for (int j = 0; j < poly2.Length; j++)
                {
                    result[i + j] += poly1[i] * poly2[j];
                }
            }

            return result;
        }

        static int[] MultiplyPolynomialsRegularParallel(int[] poly1, int[] poly2)
        {
            int degree = poly1.Length + poly2.Length - 2;
            int[] result = new int[degree + 1];

            Parallel.For(0, poly1.Length, i =>
            {
                for (int j = 0; j < poly2.Length; j++)
                {
                    result[i + j] += poly1[i] * poly2[j];
                }
            });

            return result;
        }


        static int[] MultiplyPolynomialsKaratsubaSequential(int[] poly1, int[] poly2)
        {
            int n = Math.Max(poly1.Length, poly2.Length);

            if (n <= 2)
            {
                return MultiplyPolynomialsRegularSequential(poly1, poly2);
            }

            int half = n / 2;

            int[] a0 = poly1.Take(half).ToArray();
            int[] a1 = poly1.Skip(half).ToArray();
            int[] b0 = poly2.Take(half).ToArray();
            int[] b1 = poly2.Skip(half).ToArray();

            int[] z0 = MultiplyPolynomialsKaratsubaSequential(a0, b0);
            int[] z2 = MultiplyPolynomialsKaratsubaSequential(a1, b1);

            int[] a0a1 = AddPolynomials(a0, a1);
            int[] b0b1 = AddPolynomials(b0, b1);

            int[] z1 = MultiplyPolynomialsKaratsubaSequential(a0a1, b0b1);
            z1 = SubtractPolynomials(z1, z0);
            z1 = SubtractPolynomials(z1, z2);

            int[] result = new int[2 * n - 1];

            Array.Copy(z0, result, z0.Length);
            Array.Copy(z1, 0, result, half, z1.Length);
            Array.Copy(z2, 0, result, 2 * half, z2.Length);

            return result;
        }

        static int[] MultiplyPolynomialsKaratsubaParallel(int[] poly1, int[] poly2)
        {
            int n = Math.Max(poly1.Length, poly2.Length);

            if (n <= 2)
            {
                return MultiplyPolynomialsRegularParallel(poly1, poly2);
            }

            int half = n / 2;

            int[] a0 = poly1.Take(half).ToArray();
            int[] a1 = poly1.Skip(half).ToArray();
            int[] b0 = poly2.Take(half).ToArray();
            int[] b1 = poly2.Skip(half).ToArray();

            int[] z0, z1, z2;
            z0 = new int[half * 2 - 1];
            z2 = new int[half * 2 - 1];

            // Parallelize the recursive calls for a0b0 and a1b1
            Parallel.Invoke(
                () => z0 = MultiplyPolynomialsKaratsubaParallel(a0, b0),
                () => z2 = MultiplyPolynomialsKaratsubaParallel(a1, b1)
            );

            int[] a0a1 = AddPolynomials(a0, a1);
            int[] b0b1 = AddPolynomials(b0, b1);

            z1 = MultiplyPolynomialsKaratsubaParallel(a0a1, b0b1);
            z1 = SubtractPolynomials(z1, z0);
            z1 = SubtractPolynomials(z1, z2);

            int[] result = new int[2 * n - 1];

            Array.Copy(z0, result, z0.Length);
            Array.Copy(z1, 0, result, half, z1.Length);
            Array.Copy(z2, 0, result, 2 * half, z2.Length);

            return result;
        }


        static int[] AddPolynomials(int[] poly1, int[] poly2)
        {
            int[] result = new int[Math.Max(poly1.Length, poly2.Length)];

            for (int i = 0; i < poly1.Length; i++)
            {
                result[i] = poly1[i];
            }

            for (int i = 0; i < poly2.Length; i++)
            {
                result[i] += poly2[i];
            }

            return result;
        }

        static int[] SubtractPolynomials(int[] poly1, int[] poly2)
        {
            int[] result = new int[Math.Max(poly1.Length, poly2.Length)];

            for (int i = 0; i < poly1.Length; i++)
            {
                result[i] = poly1[i];
            }

            for (int i = 0; i < poly2.Length; i++)
            {
                result[i] -= poly2[i];
            }

            return result;
        }


